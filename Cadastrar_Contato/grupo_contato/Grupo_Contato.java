    private void cadastrar_Grupo( Bean_Grupo_Contato Bean ){
        
        Control_Grupo_Contato_Cadastrar Control = new Control_Grupo_Contato_Cadastrar( Home.ip );
        Control.cadastrar( Bean );
    }
    
    private Bean_Grupo_Contato getBean(){
        Bean_Grupo_Contato Bean = new Bean_Grupo_Contato();
        
        //ID
                    
        Bean.setNOME          ( tfNome         .getText().trim() );   
        Bean.setPALAVRASCHAVE ( tfPalavrasChave.getText().trim() );  
        
        return Bean;
    }
    
    private void limparCampos(){
        
        tfNome         .setText("");
        tfPalavrasChave.setText("");
    }
    
