    private boolean um = true;
    private void setarTipoDeUsuario(){ 
        new Thread() {   @Override
            public void run() {
                while ( um == true ) { um = false;
                    try { Thread.sleep(1); verificarParaSetarTipoDeUsuario(); } catch(Exception e) { }
                }
            }
        }.start(); um = true;
    }
    
    private void verificarParaSetarTipoDeUsuario(){  
        
        Control_Listar_TipoDeUsuario Control = new Control_Listar_TipoDeUsuario( Home.ip );
        List<Bean_Tipo_de_Usuario> Bean_Listar_TipoDeUsuario = Control.listar( "%"+""+"%" );
                
        mostrarLista( Bean_Listar_TipoDeUsuario );
    }
    
    protected void mostrarLista( List<Bean_Tipo_de_Usuario> Bean_Listar_TipoDeUsuario ){
        if( cbTipoDeUsuario.getItemCount() > 0 ) { cbTipoDeUsuario.removeAllItems(); }           
        for (int i = 0; i < Bean_Listar_TipoDeUsuario.size(); i++){ 
            
            //System.out.println( i + " - " + Bean_Listar_TipoDeUsuario.get(i).getTIPODEUSUARIO() ); 
            
            cbTipoDeUsuario.addItem( Bean_Listar_TipoDeUsuario.get(i).getTIPODEUSUARIO() );
        }
    }
    
