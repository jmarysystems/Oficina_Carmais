    boolean permitirAcesso = true;
    private void configurarCbPermitirAcesso(){
        if( permitirAcesso == true ) {
            permitirAcesso = false;
            jpPermitirAcesso.setVisible( false );
        } else if( permitirAcesso == false ) {
            permitirAcesso = true;
            jpPermitirAcesso.setVisible( true );
        }
        
    }
    
    private void verificarParaCadastrarUsuario(){
        VerificarCampos_Usuario Controle = new VerificarCampos_Usuario( this );
        if( Controle.verificar() == true ) {
            
            if( chbDefinirPermissoes.isSelected() ) {
                
                definirPermissoesDeAcessoDoUsuario( getBean() );
            } 
            else {
                
                cadastrarUsuario( getBean() );
            }
            
            limparCampos();                                
        }
        
    }
    
    private void limparCampos(){
        tfNome            .setText("");
        tfEmail           .setText("");
        tfPalavrasChave   .setText("");
        ftfComissaoProduto.setText("");
        tfComissaoServico .setText(""); 
        tfLogin           .setText("");
        tfSenha           .setText("");
        tfRepetirSenha    .setText("");
    }
               
    private void cadastrarUsuario( Bean_Usuario Bean ){
        
        Control_Cadastrar Control = new Control_Cadastrar( Home.ip );
        Control.cadastrar( Bean );
    }
    
    private void definirPermissoesDeAcessoDoUsuario( Bean_Usuario Bean ){
        // receber bean e enviar para - new Definir_Permissao_Apos_Salvar_Usuario( bean )
        Home.ControleTabs.AddTabsAoHome( "Definir Permissões do Usuario", "livroTp.gif", new Definir_Permissao_Apos_Salvar_Usuario( Bean, Home ) ); 
        //Home.tabHome.setSelectedIndex( Home.tabHome.countComponents() - 1 );
    }
    
    private Bean_Usuario getBean(){
        Bean_Usuario Bean = new Bean_Usuario();
        
        //ID
                    
        Bean.setNOME                        ( tfNome.getText().trim()                       );                         
        Bean.setEMAIL                       ( tfEmail.getText().trim()                      );                           
        Bean.setPALAVRASCHAVE               ( tfPalavrasChave.getText().trim()              );                   
                    
        Bean.setTIPODEUSUARIO               ( cbTipoDeUsuario.getSelectedItem().toString()  );                     
                                            
        if( !ftfComissaoProduto.getText().trim().equals("") ){
            Float.parseFloat( ftfComissaoProduto.getText().trim() );
        }
        if( !tfComissaoServico.getText().trim().equals("") ){
            Float.parseFloat( tfComissaoServico.getText().trim() );
        }
            
        Bean.setPERMITIRACESSO               ( chbPermitirAcessar.isSelected() );
        Bean.setLOGIN                        ( tfLogin.getText().trim()        );
        Bean.setSENHA                        ( tfSenha.getText().trim()        );                                                   
                    
        Bean.setCADASTRARGRUPO              ( false );              
        Bean.setCADASTRARCLIENTE            ( false );                            
        Bean.setCADASTRARFORNECEDOR         ( false );          
        Bean.setCADASTRARTRANSPORTADOR      ( false );       
                    
        Bean.setCADASTRARSERVICO            ( false );                               
        Bean.setCADASTRARCATEGORIA          ( false );          
        Bean.setCADASTRARPRODUTO            ( false );             
                    
        Bean.setCADASTRARTIPOSDEDOCUMENTOS  ( false );   
        Bean.setCADASTRARFORMADEPAGAMENTO   ( false );     
        Bean.setCADASTRARALTERARDEPAGAMENTO ( false );  
                    
        Bean.setCADASTRAROS                 ( false );                 
        
        return Bean;
    }
    
