    private void verificarParaCadastrarServico(){
        VerificarCampos_Servico Controle = new VerificarCampos_Servico( this );
        if( Controle.verificar() == true ) {
            
            cadastrarServico( getBean() );
            
            limparCampos();                                
        }
        
    }
    
    private void cadastrarServico( Bean_Servico Bean ){
        
        Control_Servico_Cadastrar Control = new Control_Servico_Cadastrar( Home.ip );
        Control.cadastrar( Bean );
    }
    
    private void limparCampos(){
        tfNome            .setText("");
        tfPreco           .setText("");
        tfPalavrasChave   .setText("");
    }
    
    private Bean_Servico getBean(){
        Bean_Servico Bean = new Bean_Servico();
        
        //ID
                    
        Bean.setNOME                        ( tfNome.getText().trim()                       );                         
        Bean.setPRECO                       ( Float.parseFloat( tfPreco.getText().trim() )  );                           
        Bean.setPALAVRASCHAVE               ( tfPalavrasChave.getText().trim()              );  
        
        return Bean;
   
    }
    
