    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        verificarSeCategoriaJaExiste verificar = new verificarSeCategoriaJaExiste(Home, this);
        if( !tfNome.getText().trim().equals("") ) {
                if( verificar.verificarMargem() == true ) {
            
                    cadastrar_Categoria_Produto( getBean() );
                    limparCampos();
                }
        }
        else{
            JOptionPaneMod JOptionPaneMod = new JOptionPaneMod( 2, "O Campo nome da categoria não pode ser nulo", "Categoria" );
            tfNome.requestFocus();
        }        
    }//GEN-LAST:event_jButton3ActionPerformed

