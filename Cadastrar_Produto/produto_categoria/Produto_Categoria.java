    private void cadastrar_Categoria_Produto( Bean_Produto_Categoria Bean ){
        
        Control_Produto_Categoria_Cadastrar Control = new Control_Produto_Categoria_Cadastrar( Home.ip );
        Control.cadastrar( Bean );
    }
    
    private Bean_Produto_Categoria getBean(){
        Bean_Produto_Categoria Bean = new Bean_Produto_Categoria();
        
        //ID
                    
        Bean.setNOME   ( tfNome.getText().trim()                );  
        Bean.setMARGEM ( Float.parseFloat( ftmMagem.getText() ) ); 
        
        return Bean;
    }
    
    
        
